package chater1;


public class PrintData {

	public static void main(String[] args) {

		//숫자
		System.out.println(100);
		System.out.println(3.3);
		System.out.println(100 + 10); //숫자 더하기
		System.out.println("======================");
		
		//문자
		System.out.println('A'); //홀따옴표 - 문자 표기
		System.out.println("B"); //쌍따옴표 - 문자 열 표기
		System.out.println("Banana"); //쌍따옴표 - 문자 열 표기
		System.out.println(100 + "10"); //문자 연결
		System.out.println("======================");
		
		//불리안(boolean)
		System.out.println(true);
		System.out.println(4 > 5);
	}

}
